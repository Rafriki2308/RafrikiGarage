    /**
    * @return Car Returns a Car object
     */
    public function findOneCarById($value): ?Car
    {
        return $this->createQueryBuilder('c')
            ->andWhere('c.id = :val')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }
}
